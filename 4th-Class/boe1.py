# Get two numbers from the user
num1 = int(input("Enter the first number: "))
num2 = int(input("Enter the second number: "))

# Perform bitwise OR and AND operations
bitwise_or = num1 | num2
bitwise_and = num1 & num2

# Display the results
print(f"Bitwise OR of {num1} and {num2} is: {bitwise_or}")
print(f"Bitwise AND of {num1} and {num2} is: {bitwise_and}")
